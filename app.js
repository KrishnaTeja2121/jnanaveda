function pickLexemes(text, lexicon, maxItems = 3) {
  const hits = [];
  Object.entries(lexicon).forEach(([needle, gloss]) => {
    if (text.includes(needle)) hits.push(gloss);
  });
  return [...new Set(hits)].slice(0, maxItems);
}

